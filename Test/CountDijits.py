# CountDigits

n = input("Please enter a number as a type integer: ")
count = len(n) # len() = count a mount in set or arg

if count == 1 :
    print("1 digits")
elif count == 2 :
    print("2 digits")
elif count == 3 :
    print("3 digits")
elif count == 4 :
    print("4 digits")
else :
    print("other digits")
